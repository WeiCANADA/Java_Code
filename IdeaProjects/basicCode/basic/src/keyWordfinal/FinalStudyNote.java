package keyWordfinal;
/*
* final
* 常用的方法
* 1.修饰一个类
* 格式: public final class 类名称{
* }
* 含义:当前类不能有任何子类.(太监类)
*
* 2.修饰一个方法
*格式:修饰符 final fan返回值类型 方法名称(参数列表){
* //方法体
* }
* 含义:这个方法为最终方法,不能被覆盖重写.
* 注意事项:对于类和方法来书,abstract 关键字和final关键字不能同时使用.因为矛盾.
*
* 3.修饰一个局部变量
*
*  含义:对于基本类型来说,不可变是变量当中的数据不可改变
*       对于引用类型来说,final 修饰的是地址,不可变的是变量当中的地址值不可变.
*
*  3.修饰一个成员变量
*  final 修饰成员变量,必须赋值.可以通过直接赋值,也可以通过构造方法赋值.
* */
